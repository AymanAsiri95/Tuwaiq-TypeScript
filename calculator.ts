let operator;
var number1;
var number2;
var result;

console.log('Welcome ! Enter two numbers with operator (+ , - , * , /)');

if (operator == '+') {
    result = number1 + number2;
}
else if (operator == '-') {
    result = number1 - number2;
}
else if (operator == '*') {
    result = number1 * number2;
}

else {
    result = number1 / number2;
}
